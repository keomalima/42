#include "libft.h"
#include <strings.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	i;

	if (!s)
		return ;
	i = 0;
	while (i < n)
		((unsigned char *)s)[i++] = '\0';
}

//Sets n bytes of the memory area pointed to by s to zero.
